export const obtenerInfoDestinatario = (nombreResponsable) => {
    // Normalizar string para búsqueda inexacta
    const nomNormalized = nombreResponsable.trim().toLowerCase();
    
    // Buscar en el diccionario (por login ID o por nombre completo)
    const usuario = USUARIOS.find(u => 
        u.usuario.toLowerCase() === nomNormalized || 
        u.nombre.toLowerCase() === nomNormalized
    );
    
    if (!usuario) {
        return null;
    }

    let ccs = [];
    if (usuario.organizacion === "TI") {
        ccs = JEFES.TI;
    } else if (usuario.organizacion === "ADMINISTRATIVO") {
        ccs = JEFES.ADMINISTRATIVO;
    }

    return {
        email: usuario.email,
        cc: ccs
    };
};
